                    <h6 id="author" class="mt-4">1. AUTHOR(S)' RESPONSIBILITIES</h6>
                    <p>
                        1.1. Paper must be presented in English.<br/>
                        1.2. Paper must be of a standard acceptable for publication.<br/>
                        1.3. Work presented should not have been previously used in part or whole for another publication on another platform.
                    </p>
                    <h6 id="submission" class="mt-4">2. SUBMISSION OF ARTICLES</h6>
            <p>
                2.1 Soft copy<br/>
                Articles should be submitted as an attachment via e-mail to: ajtem@htu.edu.gh and should comply with the following:<br/>
                2.1.1 File formats: The preferred softcopies format for new submission and re-submission of revised manuscript is
                PDF format. Please submit only the final version of your file (with no hidden text).<br/>
                2.1.2 Graphics: All figures should be embedded and correctly positioned within your PDF files in JPEG or
                GIF format. Scanned Art work, both line and half-tone photographs should be scanned at minimum settings
                of 300 dpi for half-tones and 800 dpi for line art. Compressed files should be in WinRAR.<br/>
                2.1.3 File naming: To protect their anonymity in the review process, AUTHORS SHOULD NOT IDENTIFY THEMSELVES
                ON THE FRONT PAGE OR IN ANY HEADERS OR WHILE SAVING THE NAMES OF THEIR FILES. A SEPARATE TITLE PAGE MUST
                BE SENT AS AN ATTACHMENT TO THE EDITORS via the EMAIL ADDRESS ABOVE.<br/>
                2.1.4 Title page<br/>
                The title page should contain (i) the title of the paper, (ii) the full names of the author(s) and (iii)
                the addresses of the institutions at which the work was carried out together with (iv) the full postal
                and email address, Fax and telephone numbers, of the corresponding author. The present address of any
                author, if different from that where the work was carried out, should be supplied.<br/>
                A short running title (less than 40 characters) should also be provided.
            </p>
                    <h6 id="typesetting" class="mt-4">
                        3. TYPESETTING
                    </h6>
                    <p>
                        3.1 Articles should be typeset as follows:<br/><br/>
                        Paper size: A4.<br/></p>
                    <ul>
                        <li><i class="fa fa-check"> Paper size: A4.</i></li>
                        <li><i class="fa fa-check"> Margins: Left 2.5 cm, Right 2.0 cm; Top: 2.0 cm Bottom 2.0 cm (For both text, tables and figures).</i></li>
                        <li><i class="fa fa-check"> Paper orientation: Portraits (except for broader tables or figures with captions only)</i></li>
                        <li><i class="fa fa-check"> Font:</i></li>
                        <li><i class="fa fa-check"> Heading: (Type) Times New Roman, Size 14, bold, Capitalize Each Word except prepositions</i></li>
                        <li><i class="fa fa-check"> Sub – heading: (Type) Times New Roman, Size 12, bold, Capitalize Each Word except prepositions</i></li>
                        <li><i class="fa fa-check"> Sub sub-heading: (Type) Times New Roman, Size 12, italics, Capitalize Each Word except prepositions</i></li>
                        <li><i class="fa fa-check"> Text: (Type) Times New Roman, Size 11, normal.</i></li>
                        <li><i class="fa fa-check"> Line spacing: 1.5 spacing.</i></li>
                        <li><i class="fa fa-check"> Indent: flush left</i></li>
                        <li><i class="fa fa-check"> Table and Figure captions: (Type) Times New Roman, Size 11, bold, Capitalize Each Word except prepositions</i></li>
                    </ul>
                    <p>3.2 Insert one line space between paragraphs, and two line spaces between paper title and abstract on the first page.</p>
                    <p>3.3 The title of the paper, abstract and keywords should be set out following the notes in section 4 below. Do <strong>NOT</strong> number or letter section headings.</p>
                    <p>3.4 The maximum acceptable length of an article is 5000 words (less 250 words for each normal-sized figure or table you include):
                        please do NOT exceed this limit or your paper will be rejected.</p>

                    <h6 id="content" class="mt-4">4. CONTENT</h6>
                    <ul>
                    <li><i class="fa fa-check"> Use of technical language.</i></li>
                    <li><i class="fa fa-check"> Use of active words. Avoid the use of passive words. Ensure consistency in tense.</i></li>
                    <li><i class="fa fa-check">  Include Research limitations</i></li>
                    <li><i class="fa fa-check">  Include Social, Economic, Policy, Managerial or Practical Implications if applicable</i></li>
                    </ul>
                   <p>4.1.Tentative Structure of Paper</p>
                    <p>4.2.Title of Paper</p>
                    <ul>
                    <li><i class="fa fa-circle"> Do not use abbreviations in the title.</i></li>
                    </ul>

                    <p>4. 3.Abstract</p>
                    <ul>
                        <li><i class="fa fa-circle">  Ensure your abstract is not more than 200 words.</i></li>
                        <li><i class="fa fa-circle"> Ensure that the abstract contains the following:</i></li>
                        <li><i class="fa fa-check"> Aim/Objective of the study – Best to state the main objective only</i></li>
                        <li><i class="fa fa-check"> Methodology</i></li>
                        <li><i class="fa fa-check">  Key Findings</i></li>
                        <li><i class="fa fa-check"> Implications of Findings</i></li>
                        <li><i class="fa fa-check"> Originality and Value of the study</i></li>

                    </ul>
                    <p>4.4 Introduction</p>
                    <ul>
                        <li><i class="fa fa-check"> Outline clearly the motivation for the study. In doing so, provide
                                a global perspective on the proposed study to ensure a wider readership of the paper.
                                Avoid a narrow perspective to the study.</i></li>
                        <li><i class="fa fa-check"> State clearly the theoretical gap in the literature which this study fills.</i></li>
                        <li><i class="fa fa-check"> Provide a brief but succinct account of the contextualisation of the study.
                                You may consider creating a separate section to discuss the contextualisation of the study.</i></li>
                        <li><i class="fa fa-check"> Outline the contributions (theoretical, empirical, policy) of this study</i></li>
                        <li><i class="fa fa-check"> Outline the structure of the paper briefly.</i></li>
                    </ul>

                    <p>4.5 Literature Review</p>

                    <ul>
                        <li><i class="fa fa-check"> Provide a critical review of the extant literature, ensuring that you discuss the theoretical
                                debates and the gaps in the literature. It is important to review seminal articles</i></li>
                    </ul>
                    <p>4.6. Methodology</p>

                    <p>4.7. Results and Discussion</p>
                    <ul>
                    <li><i class="fa fa-check"> For empirical studies, it is ideal to have a sub-section on robustness checks.</i></li>
                    <li><i class="fa fa-check"> Compare and contrast the empirical results of this study with</i></li>
                    </ul>
                    <p>4.8. Conclusion</p>
                    <ul>
                        <li>
                            <li><i class="fa fa-check"> Avoid citations in this section of the paper unless the paper
                                is seminal and the purpose of your paper is to test a hypothesis proposed by this study</i></li>
                        <li><i class="fa fa-check">Summarise the key findings of the study</i></li>
                            <li><i class="fa fa-check">State clearly the implications of the findings under the headings
                                    (if appropriate): Practical Implications, Managerial Implications, Policy Implications</i></li>
                            <li><i class="fa fa-check">State the limitations of the study under a sub-heading of Limitations of the Study.</i></li>
                    </ul>
                    <p>4.9. Classification of the Paper</p>
                    <ul>
                        <li><i class="fa fa-check"> Research Paper</i></li>
                        <li><i class="fa fa-check"> Technical Paper</i></li>
                        <li><i class="fa fa-check"> Case Study</i></li>
                        <li><i class="fa fa-check"> Literature Review</i></li>
                        <li><i class="fa fa-check"> Conceptual Paper</i></li>
                        <li><i class="fa fa-check"> Comment on Paper published in AJTEM</i></li>
                        <li><i class="fa fa-check"> Viewpoint</i></li>
                    </ul>
                    <p>4.10.  References:</p>

                    <p>These should be made to accessible sources. Please ensure that all works cited in the text are
                    included in the reference list, and that the dates and authors  given in the text match those
                    in the reference list. References must always be given in sufficient detail for the reader to locate
                        the works cited (refer to section 7     below for formats).</p>
                    <h6>NB:</h6>
                    <p>Before Submission:</p>
                    <ul>
                        <li><i class="fa fa-check"> Edit your work</i></li>
                        <li><i class="fa fa-check"> Undertake peer review, if necessary</i></li>
                        <li><i class="fa fa-check"> Undertake professional editing, if relevant</i></li>
                        <li><i class="fa fa-check"> Ensure the manuscript is free of grammatical and typographical errors</i></li>
                    </ul>

                    <h6 id="nomenclature" class="mt-4">5. NOMENCLATURE/UNITS</h6>
                    <p>
                    5.1 Please ensure that all terminologies and notations used are widely understood. Avoid author-invented
                        acronyms and abbreviations. Abbreviations and acronyms should be written-out in full at their first
                        occurrence in the text (note that where an abbreviation has been written-out in full in the abstract,
                        it must still be written-out in full in the text the first time it is used). Abbreviate units of measure
                        when used with numerals (50ml; 100kg). Chemical formulae should be written out unless they are used to
                        economise space in the column headings of a table, however, the formulae should be expanded in the footnotes.<br/><br/>
                    5.2 SI units should be used in all cases. If non-SI units must be used, SI equivalents (or conversion factors)
                        must also be given. Please use the spellings “litre” and “metre” (a “meter” is a measuring instrument).<br/><br/>
                    5.3 Please use a decimal point rather than a comma in numbers (i.e. 3.142 not 3,142).<br/><br/>
                    5.4 Write equations in dimensionless form or in metric units. Please use italics to denote variables
                        (in text or in displayed equations).
                    </p>
                    <h6 id="figures" class="mt-4">6. FIGURES AND TABLES</h6>
                    <p>
                        6.1 Figures and tables should appear in numerical order, be described in the body of the text
                        and be positioned close to where they are first cited.<br/><br/>
                        6.2 Make sure all figures and tables fit inside the text area.<br/><br/>
                        6.3 Because figures may be re-sized in the course of production please use scale bars and not
                        magnification factors.<br/><br/>
                        6.4 Art works should be drawn and lettered for finished size and subsequent reduction to 75% using
                        a Times New Roman (type). Giving a final size after reduction of 8 point (type) with appropriate line weights.<br/><br/>
                        6.5 Table captions should be at the top of tables while Figures are labelled below the diagrams.<br/><br/>
                        6.6 Tables should be displayed in APA format<br/><br/>
                        6.7 All illustrations (line drawings and photographs) are classified as figures. Provide all
                        figures in black and white (monochrome colours)<br/><br/>
                    </p>
                    <h6 id="references" class="mt-4">7. REFERENCES: CITATIONS IN TEXT</h6>
                    <p>
                        7.1 Use surname of author and year of publication: Jones (2002) or (Jones, 2002).<br/><br/>
                        7.2 Insert initials only if there are two different authors with the same surname and same year of publication.<br/><br/>
                        7.3 Two or more years in parentheses following an author's name are cited in ascending order of
                        year, and two or more references published in the same year by the same author are differentiated
                        by letters a, b, c, etc. For example: Brown (1999, 2002, 2003a, b).<br/><br/>
                        7.4 Different references cited together should be in date order, for example: (Smith, 1959;
                        Thomson and Jones, 1992; Green, 1999).<br/><br/>
                        7.5 If a paper has been accepted for publication but has not been published the term "(in press)"
                        should be used instead of a date.<br/><br/>
                        7.6 If a paper has been submitted but not definitely accepted the term "(submitted)" should be used.
                        If the paper is still being prepared the term "(in preparation)" should be used.<br/><br/>
                        7.7 The abbreviation "et al.," should be used in the text when there are more than two co-authors of a cited paper.
                    </p>
                    <h6 id="references" class="mt-4">8.  LIST OF REFERENCES</h6>
                    <p>
                        8.1 References should be listed alphabetically at the end of the paper. Although "et al." is preferable in the text, in the list of references all authors should be given.
                        <br>

                        8.2 Journal Reference Style: <br>

                        Note the order: Name of author(s). (year of publication). “Title of article”. Name of Journal. Volume (Issue number), first page-last page of article.
                        <br>

                        Behn, R. D. (2003). "Why Measure Performance? Different Purposes Require Different <br>
                        Measures". Public Administration Review, 63(5), 586-606. <br>

                        Zeng R. J., Lemaire R., Yuan Z. and Keller J. (2004). A novel wastewater treatment process: <br>
                        simultaneous nitrification, denitrification and phosphorus removal. Water Science and Technology, 50(10), 163-170.
                        <br>

                        8.3 Book Reference Styles: <br>

                        Bell J. (2002). Treatment of Dye Wastewaters in the Anaerobic Baffled Reactor and <br>
                        Characterisation of the Associated Microbial Populations. PhD thesis, Pollution Research Group, Durban, University of Natal.
                        <br>

                        Henze M., Harremoës P., LaCour Jansen J. and Arvin E. (1995). Wastewater Treatment: <br>
                        Biological and Chemical Processes. Heidelberg. Springer. <br>

                        McInerney M. J. (1999). Anaerobic metabolism and its regulation. In: Biotechnology, J. Winter
                        <br>
                        (ed.), 2nd edn, Weinheim, Wiley-VCH Verlag, pp. 455-478. <br>

                        Sobsey M. D. and Pfaender F. K. (2002). Evaluation of the H2S method for Detection of Faecal
                        <br>
                        Contamination of Drinking Water, Report WHO/SDE/WSH/02.08, Water Sanitation and Health Programme, Geneva, WHO.
                        <br>

                        Standard Methods for the Examination of Water and Wastewater (1998). 20th Edn., <br>
                        Washington DC, American Public Health Association/American Water Works Association/Water Environment Federation.
                        <br>

                        8.4 Online references: these should specify the full URL for the reference and give the date
                        <br>
                        on which it was consulted or accessed . <br>

                        Alcock S. J. and Branston L. (2000). SENSPOL: Sensors for Monitoring Water Pollution from <br>
                        Contaminated Land, Landfills and Sediment. http://www.cranfield.ac.uk/biotech/senspol/ (Accessed: 22 July 2005)
                        <br>

                        8.5 References in languages other than English should be accompanied by an English translation of the article title:
                        <br>

                        Barjenbruch M., Erler C., and Steinke M. (2003). Untersuchungen an Abwasserteichanlagen <br>
                        in Sachsen-Anhalt im Jahr 2003 (Investigation on wastewater lagoons in Saxony-Anhalt in 2003), Magdeburg, Report for the Environment Ministry of Saxony-Anhalt.
                        <br>
                    </p>

                    <h6 id="software" class="mt-4">9. SOFTWARE CITATION</h6>
                    <p>
                        Authors should cite Software developers in parenthesis in the text after the first mention of a software package.
                        <br>
                        Software citations should include the name, version number and release date of the software as well as the name of
                        <br>
                        the software developer. Example: “The data was analysed using the STATA statistical software (version 13.1, 2013,
                        <br>
                        StataCorp LP)”.
                    </p>

                    <h6 id="footnote" class="mt-4">10. FOOTNOTES</h6>
                    <p>
                        Footnotes should only be used if absolutely essential. Generally, incorporate all material in normal text.
                        <br>
                        If used, footnotes should be brief and numbered consecutively in plain superscripts. <br>
                        Exceptions include initial footnotes associated with the title of the article or author’s name, denoted by other symbols apart from asterisk.
                        <br>
                        <br>
                        FOOTNOTES TO A TABLE <br><br>

                        Use lower case English letters to attach footnotes to specific items within the table, <br>
                        and place the footnotes below the bottom line of the table in (un-indented) paragraph form. <br>
                        For general explanatory notes, use the heading “Note:” and continue on the same line with the first word of the note,
                        <br>
                        in paragraph form. Reserve the use of asterisk (e.g., *, ** or ***) to denote statistical significance levels.
                        <br>
                        For example, "Asterisk (*) and double asterisk (**) denote variables significant at 10% and 5% respectively."
                        <br>
                    </p>

