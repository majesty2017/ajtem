                    <h6>MANUSCRIPTS GUIDELINES FOR AUTHORS</h6>
                    <p>
                        Papers submitted for publication are expected to meet the following guidelines. Papers submitted
                        will be peer-reviewed and might be recommended for publication.
                    </p>
                    <ul>
                        <li><a href="#author"> <i class="fa fa-check"></i> AUTHOR(S)' RESPONSIBILITIES</a></li>
                        <li><a href="#submission"> <i class="fa fa-check"></i>SUBMISSION OF ARTICLES</a> </li>
